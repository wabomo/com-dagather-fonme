package com.dagather.fonme.exchange.pojo;

public class FinanceRateCode {
    private String status;
    private String scur;
    private String tcur;
    private String ratenm;
    private String rate;
    private String update;

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getScur() {
        return scur;
    }

    public void setScur(String scur) {
        this.scur = scur;
    }

    public String getTcur() {
        return tcur;
    }

    public void setTcur(String tcur) {
        this.tcur = tcur;
    }

    public String getRatenm() {
        return ratenm;
    }

    public void setRatenm(String ratenm) {
        this.ratenm = ratenm;
    }

    public String getRate() {
        return rate;
    }

    public void setRate(String rate) {
        this.rate = rate;
    }

    public String getUpdate() {
        return update;
    }

    public void setUpdate(String update) {
        this.update = update;
    }
}
